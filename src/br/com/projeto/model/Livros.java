
package br.com.projeto.model;

public class Livros {
    private int id;
    private String titulodaobra;
    private String autor;
    private String editora;
    private String colecao;
    private int numerodepaginas;
    private int nota;
    private String recomendacao;
    private String recomendaria;
    private String genero;
    private String filme; 
    private String personagemfavorito;
    private int capitulos;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitulodaobra() {
        return titulodaobra;
    }

    public void setTitulodaobra(String titulodaobra) {
        this.titulodaobra = titulodaobra;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public String getEditora() {
        return editora;
    }

    public void setEditora(String editora) {
        this.editora = editora;
    }

    public String getColecao() {
        return colecao;
    }

    public void setColecao(String colecao) {
        this.colecao = colecao;
    }

    public int getNumerodepaginas() {
        return numerodepaginas;
    }

    public void setNumerodepaginas(int numerodepaginas) {
        this.numerodepaginas = numerodepaginas;
    }

    public int getNota() {
        return nota;
    }

    public void setNota(int nota) {
        this.nota = nota;
    }

    public String getRecomendacao() {
        return recomendacao;
    }

    public void setRecomendacao(String recomendacao) {
        this.recomendacao = recomendacao;
    }

    public String getRecomendaria() {
        return recomendaria;
    }

    public void setRecomendaria(String recomendaria) {
        this.recomendaria = recomendaria;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }

    public String getFilme() {
        return filme;
    }

    public void setFilme(String filme) {
        this.filme = filme;
    }

    public String getPersonagemfavorito() {
        return personagemfavorito;
    }

    public void setPersonagemfavorito(String personagemfavorito) {
        this.personagemfavorito = personagemfavorito;
    }

    public int getCapitulos() {
        return capitulos;
    }

    public void setCapitulos(int capitulos) {
        this.capitulos = capitulos;
    }


}
